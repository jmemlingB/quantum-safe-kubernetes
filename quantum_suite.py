# SCRIPT C: RAFT COLLISION SIMULATOR (GAIN PROOF)
# ==========================================
def simulate_raft(iterations=100000):
    legacy_coll = 0
    quantum_coll = 0
    for _ in range(iterations):
        # Legacy: Integer clumping
        l_timeouts = np.random.randint(150, 301, 5)
        if np.sort(l_timeouts)[0] == np.sort(l_timeouts)[1]: legacy_coll += 1

        # Quantum: Continuous resolution
        q_timeouts = np.random.uniform(150, 300, 5)
        if (np.sort(q_timeouts)[1] - np.sort(q_timeouts)[0]) < 0.44: quantum_coll += 1

    l_rate = (legacy_coll / iterations) * 100
    q_rate = (quantum_coll / iterations) * 100
    gain = ((l_rate - q_rate) / l_rate) * 100

    print(f"\nSimulating {iterations:,} Raft Cycles...")
    print(f"Legacy Collision Rate:  {l_rate:.2f}%")
    print(f"Quantum Collision Rate: {q_rate:.2f}%")
    print(f"Net Performance Gain:   {gain:.1f}%")

